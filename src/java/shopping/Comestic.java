package shopping;

/**
 *
 * @author PC
 */
public class Comestic {
    private String id;
    private String name;
    private double price;
    private int quantity;
    private String images;

    public Comestic() {
    }

    public Comestic(String id, String name, double price, int quantity) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.quantity = quantity;
    }

    public Comestic(String id, String name, double price, int quantity, String images) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.quantity = quantity;
        this.images = images;
    }

    public String getImages() {
        return images;
    }

    public void setImages(String images) {
        this.images = images;
    }


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
